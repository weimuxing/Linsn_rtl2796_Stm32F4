static BYTE SendcardCheckSum(BYTE *buffer, int size)
{
	u32 sum = 0;
	int i;
	for(i=0; i<size; i++)
	{
		sum += buffer[i];
	}
	return (BYTE)sum;
}

static u32 SendcardCheckSum_u32(BYTE *buffer, int size)
{
	u32 sum = 0;
	int i;
	for(i=0; i<size; i++)
	{
		sum += buffer[i];
	}
	return sum;
}




